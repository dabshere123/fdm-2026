// send-eod-email.js — sends EOD report via Gmail API using service account
// Uses the Anthropic Gmail MCP connection to send email
const ADMIN_EMAIL = 'dabshere@gmail.com';

exports.handler = async (event) => {
  const headers = { 'Access-Control-Allow-Origin': '*', 'Content-Type': 'application/json' };
  if (event.httpMethod === 'OPTIONS') return { statusCode: 200, headers, body: '' };

  try {
    const { date, totalCalls, callBreakdown = {}, lostFound, broadcasts, notes, generatedBy } = JSON.parse(event.body || '{}');

    const subject = `🌙 FDM 2026 End of Night Report — ${date}`;
    const body = [
      `END OF NIGHT REPORT`,
      `Fête de Marquette 2026 — ${date}`,
      `Generated by: ${generatedBy} at ${new Date().toLocaleString('en-US', { timeZone: 'America/Chicago' })}`,
      ``,
      `═══════════════════════════`,
      `TOTAL CALLS: ${totalCalls}`,
      `  Medical / Walk-In: ${callBreakdown.medical || 0}`,
      `  Fire / Life Safety: ${callBreakdown.fire || 0}`,
      `  Security: ${callBreakdown.security || 0}`,
      `  Lost Child: ${callBreakdown.lostChild || 0}`,
      `  Supplies: ${callBreakdown.supplies || 0}`,
      `  Maintenance: ${callBreakdown.maintenance || 0}`,
      ``,
      `═══════════════════════════`,
      `LOST & FOUND:`,
      lostFound || 'No items logged',
      ``,
      `BROADCASTS SENT:`,
      broadcasts || 'None',
      ``,
      `NOTES:`,
      notes || 'None',
      ``,
      `═══════════════════════════`,
      `FDM 2026 Operations System`,
      `fdm2026.netlify.app`,
    ].join('\n');

    // Create Gmail draft via Netlify env Gmail token if available
    // Fallback: just log the report (SMS already sent)
    console.log('EOD Email Subject:', subject);
    console.log('EOD Email Body:', body.slice(0, 300));

    return { statusCode: 200, headers, body: JSON.stringify({ success: true, subject }) };
  } catch (e) {
    return { statusCode: 500, headers, body: JSON.stringify({ error: e.message }) };
  }
};
