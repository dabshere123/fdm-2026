// send-eod-report.js
// Saves End of Night report to Airtable and sends SMS summary to Admin
// POST body: { date, totalCalls, callBreakdown, lostFound, broadcasts, notes, generatedBy }

const AIRTABLE_BASE  = 'appUVEp7kO9NeeJh0';
const AIRTABLE_TOKEN = process.env.AIRTABLE_TOKEN;
const TWILIO_SID     = process.env.TWILIO_ACCOUNT_SID;
const TWILIO_TOKEN   = process.env.TWILIO_AUTH_TOKEN;
const TWILIO_FROM    = process.env.TWILIO_PHONE_NUMBER;
const ADMIN2_PHONE   = '+16082289692';

async function sendSMS(to, message) {
  const auth = Buffer.from(`${TWILIO_SID}:${TWILIO_TOKEN}`).toString('base64');
  await fetch(`https://api.twilio.com/2010-04-01/Accounts/${TWILIO_SID}/Messages.json`, {
    method: 'POST',
    headers: { 'Authorization': `Basic ${auth}`, 'Content-Type': 'application/x-www-form-urlencoded' },
    body: new URLSearchParams({ To: to, From: TWILIO_FROM, Body: message }).toString()
  });
}

exports.handler = async (event) => {
  const headers = { 'Access-Control-Allow-Origin': '*', 'Content-Type': 'application/json' };
  if (event.httpMethod === 'OPTIONS') return { statusCode: 200, headers, body: '' };
  if (event.httpMethod !== 'POST') return { statusCode: 405, headers, body: 'Method not allowed' };

  const {
    date, totalCalls, callBreakdown, lostFound,
    broadcasts, notes, generatedBy, generatedAt, detailedCalls
  } = JSON.parse(event.body || '{}');

  const breakdown = callBreakdown || {};

  // Build full report text
  const reportText = [
    `END OF NIGHT REPORT — ${date}`,
    `Generated by: ${generatedBy} at ${generatedAt}`,
    ``,
    `TOTAL CALLS: ${totalCalls}`,
    `  Medical / Walk-In: ${breakdown.medical || 0}`,
    `  Fire / Life Safety: ${breakdown.fire || 0}`,
    `  Security: ${breakdown.security || 0}`,
    `  Lost Child: ${breakdown.lostChild || 0}`,
    `  Supplies: ${breakdown.supplies || 0}`,
    `  Maintenance: ${breakdown.maintenance || 0}`,
    ``,
    `DETAILED CALL LOG:`,
    detailedCalls || 'None',
    ``,
    `LOST & FOUND:`,
    lostFound || 'None',
    ``,
    `BROADCASTS SENT:`,
    broadcasts || 'None',
    ``,
    `NOTES:`,
    notes || 'None',
  ].join('\n');

  // Save to Airtable OvernightReports table
  try {
    await fetch(`https://api.airtable.com/v0/${AIRTABLE_BASE}/EODReports`, {
      method: 'POST',
      headers: { 'Authorization': `Bearer ${AIRTABLE_TOKEN}`, 'Content-Type': 'application/json' },
      body: JSON.stringify({
        fields: {
          EventDay: date,
          GeneratedBy: generatedBy,
          TotalCalls: totalCalls,
          Narrative: reportText,
          Incidents: `Medical: ${breakdown.medical || 0}, Fire: ${breakdown.fire || 0}, Security: ${breakdown.security || 0}, Lost Child: ${breakdown.lostChild || 0}`,
          Notes: notes || '',
          GeneratedAt: generatedAt || new Date().toISOString(),
        }
      })
    });
  } catch(e) {
    console.log('Airtable save error:', e.message);
  }

  // Send SMS summary to Admin
  const smsSummary = [
    `🌙 END OF NIGHT — ${date}`,
    `Total Calls: ${totalCalls}`,
    `Medical: ${breakdown.medical || 0} · Fire: ${breakdown.fire || 0} · Security: ${breakdown.security || 0}`,
    `Lost Child: ${breakdown.lostChild || 0} · L&F Items: ${(lostFound || '').split('\n').filter(l=>l.trim()).length}`,
    `Broadcasts: ${(broadcasts || '').split('\n').filter(l=>l.trim()).length}`,
    notes ? `Notes: ${notes.slice(0, 100)}` : '',
    `— ${generatedBy}`,
  ].filter(Boolean).join('\n');

  try {
    await sendSMS(ADMIN2_PHONE, smsSummary);
  } catch(e) {
    console.log('SMS error:', e.message);
  }

  // Send SMS to Gary as well
  try {
    await sendSMS('+16082352925', smsSummary);
  } catch(e) {
    console.log('SMS Gary error:', e.message);
  }

  // Send email to dabshere@gmail.com via Netlify function
  try {
    const emailBody = reportText;
    // Use fetch to a simple email endpoint or save to Gmail draft via API
    // For now we'll use a simple mailto-style via SendGrid if available
    // Otherwise log the report (full text is in Airtable)
    console.log('EOD Report Email Body:', emailBody.slice(0, 200));
  } catch(e) {
    console.log('Email error:', e.message);
  }

  return {
    statusCode: 200,
    headers,
    body: JSON.stringify({ success: true, report: reportText })
  };
};
